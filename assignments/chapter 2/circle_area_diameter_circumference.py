print("\nTHIS PROGRAM COMPUTES THE AREA, DIAMETER AND CIRCUMFERENCE OF A CIRCLE")


radius = 2
pi = 3.14159

print("\nAREA OF THE CIRCLE")

area = pi * (radius ** 2)
print("The area of the circle is =",area)

print("\nDIAMETER OF THE CIRCLE")

diameter = 2 * radius
print("The diameter of the circle is =",diameter)

print("\nCIRCUMFERENCE OF THE CIRCLE")

circumference = 2 * pi * radius
print("The diameter of the circle is =",circumference)





